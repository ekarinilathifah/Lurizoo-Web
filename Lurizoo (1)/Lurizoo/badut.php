                    <p>
                      Ikan badut merupakan ikan karang tropis yang hidup diperairan hangat pada daerah terumbu karang dengan kedalaman kurang dari 50 cm dan berair jernih. Daerah penyebarannya meliputi Samudra pasifik (Fiji), Laut Merah, Samudra Hindia (Indonesia, Malaysia, Thailand, Maladewa, Burma) Dan Great Barrier Reef Autralia. Ikan badut merupakan ikan yang mempunyai penyebaran relatif luas, terutama di daerah Indo Pacific. Beberapa faktor yang mempengaruhi distribusi dan arah distribusi dari ikan badut adalah jumlah larva, ketersediaan anemon laut, faktor-faktor hidrografi, dan adanya daratan penghalang.
                    </p>
                    <p>
                      Ikan badut atau clownfish berasal dari keluarga Pomacentridae yang terdiri dari dua genus yaitu Premnas dan Amphiprion. Khusus jenis Premnas hanya ditemukan 1 jenis yaitu Premnas biaculeatus (Bloch, 1790) yang biasa dikenal dengan sebutan ikan badut merah maroon. Untuk jenis Amphiprion sendiri terdiri dari 29 spesies, 13 diantaranya ditemukan di perairan Indonesia yaitu Amphiprion polymnus (Linnaeus, 1758), Amphiprion ephippium (Bloch, 1790), Amphiprion percula (Lacepede, 1802), Amphiprion ocellaris, Amphiprion chrysopterus (Cuvier, 1830), Amphiprion clarkii (Bennett, 1830), Amphiprion melanopus (Bleeker, 1852), Amphiprion sebae, Amphiprion akallopisos (Bleeker, 1853), Amphiprion perideralon (Bleeker, 1855), Amphiprion frenatus (Brevoort, 1856), Amphiprion sandaracinos (Allen, 1972), dan Amphiprion leucokranos (Allen, 1973).
                    </p>
                    <p>
                      Ikan badut hidup bersimbiosis dengan anemon laut. Anemon laut memberikan tempat tinggal bagi ikan badut, dan ikan badut membantu mengusir ikan kupu-kupu (butterfly fish) yang mencoba memakan tentakel anemon. Namun, berdasarkan penelitian terbaru memperlihatkan bahwa ikan badut mengeluarkan zat/sisa pencernaan yang dapat digunakan oleh anemon laut sebagai nutrisi penting dan sebagai gantinya, ikan badut bisa bersarang diantara tentakel anemon yang menyengat sebagai perlindungan. Ikan badut juga tetap aktif di malam hari bergerak dan mengibaskan siripnya. Kebiasaan ini berfungsi memberikan sirkulasi udara bagi anemon laut pada malam hari ketika kadar oksigen menjadi rendah dimana proses fotosintesis telah berhenti. Ukuran tubuh ikan badut pada fase dewasa berkisar 14 hingga 16 cm, pada beberapa literatur dapat mencapai 18 cm. Ikan ini menjadikan plankton dan alga sebagai makanannya sehingga tergolong omnivora. Ikan ini hidup berkoloni, dimana pada fase awal pertumbuhan semua ikan berjenis kelamin jantan. Saat memasuki fase reproduksi, salah satu dari mereka akan berubah kelamin menjadi betina. setelah bertelur, ikan jantan bertugas menjaga telur hingga fase juvenile.
